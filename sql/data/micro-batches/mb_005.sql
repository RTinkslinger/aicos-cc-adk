### Harshit’s Background & Alle Journey

- Co-founded Alle (AI personal stylist) after 5 years at Meesho (2017-2022)

- Scaled to 1.5M installs, 250K MAU, won Google Play Store awards 2024

- Journey: 3 major pivots over 2+ years before shutting down

  - Started with AI-first fashion Pinterest concept
  - Pivoted to screenshot organization app
  - Final pivot to AI stylist after manual WhatsApp experiments

### Key Learnings from Consumer Commerce

- Building conviction requires lived experience

  - Can’t build conviction through top-down thinking alone
  - Need either personal experience or deep customer interaction
  - Manual WhatsApp experiments with 100+ users gave conviction for AI stylist

- Founder-market fit critical for progress

  - Tried solar market but couldn’t make progress (no energy/flow state)
  - AI space felt accessible, could move fast and iterate
  - If no progress in defined timeframe (3-6 months), switch markets

- Retention benchmarks for shopping apps

  - Alle achieved 15% month-1 and month-6 retention
  - Myntra/established platforms at ~25% retention
  - 60% gap proved difficult to close

### Product & User Insights

- Target user: Female, 18-25, Gen Z discovering personal style

- Key use cases: Confusion states when browsing, styling recently purchased items

- Conversational semantic search was stickiest feature

  - Users could upload photos, ask multimodal styling questions
  - Surfaced complementary items and shoppable products

- Competing against friends/family advice, Pinterest inspiration, traditional search

  - Pinterest had great inspiration but wasn’t shoppable
  - Traditional platforms poor at semantic/complementary searches

### Operational Mistakes & Distractions

- Never charged users during discovery phase

  - Philosophy: wait for product maturity before monetization
  - Started charging too late (last 6-9 months)

- Hiring consumed excessive time and focus

  - Built team of 20 people
  - 3-month cycles per hire with high interview bar
  - Should have stayed leaner to move faster

- Multiple simultaneous pivots diluted focus

  - Split attention between retention, CAC, monetization experiments
  - Each distraction led to hiring needs and further complexity



